package br.gov.caixa.vitrine.entities;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@Table(name="EVJTB003_TIPO_OBJETO")
@SequenceGenerator(name = "tipoSeqGenerator", sequenceName = "TIPO_SEQ", allocationSize = 1, initialValue = 0)
public class TipoObjeto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 66863957630412366L;
	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "tipoSeqGenerator")
	private Integer codigo;
	private String descricao;

	public Integer getCodigo() {
		return codigo;
	}

	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((codigo == null) ? 0 : codigo.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		TipoObjeto other = (TipoObjeto) obj;
		if (codigo == null) {
			if (other.codigo != null)
				return false;
		} else if (!codigo.equals(other.codigo))
			return false;
		return true;
	}

}
